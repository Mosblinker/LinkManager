    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed
        for (AutoTimerMenu menu : new AutoTimerMenu[]{timerMenu1,timerMenu2}){
            System.out.println(menu.getName() + ": ");
            System.out.println("\ttoString(): "+menu.toString());
            System.out.println("\tRunning: " + menu.isRunning());
            System.out.println("\tWas Running: "+menu.wasRunning());
            System.out.println("\tPaused: " + menu.isPaused());
            System.out.println("\tEnabled: " + menu.isEnabled());
            System.out.println("\tDuration Index: " + menu.getDurationIndex());
            System.out.println("\tDuration: " + menu.getDuration());
            System.out.println("\tTimer Delay: " + menu.getTimer().getDelay());
            System.out.println("\tTimer Initial Delay: " + menu.getTimer().getInitialDelay());
            System.out.println("\tStartable: " + menu.isStartable());
            System.out.println("\tRepeating: " + menu.isRepeating());
            System.out.println();
        }
    }//GEN-LAST:event_printButtonActionPerformed

