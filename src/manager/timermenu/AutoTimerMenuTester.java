    private abstract class TimerMenu extends AutoTimerMenu{
        
        public TimerMenu(String text, int index){
            super(text,index);
        }
        
        public TimerMenu(String text){
            super(text);
        }
        
        @Override
        protected int[] getDurations() {
            return DURATIONS;
        }
        @Override
        protected String getTimerCommand() {
            return "Timer";
        }

        @Override
        protected String getDurationCommandPrefix() {
            return "Duration";
        }
        
        @Override
        protected abstract boolean isRepeating();
        
        @Override
        protected String getDurationTextPrefix(){
            return (isRepeating()) ? "Every " : "After ";
        }
    }
    
    private class RepeatingTimerMenu extends TimerMenu{
        
        public RepeatingTimerMenu(){
            super("Menu 1");
        }
        @Override
        protected boolean isRepeating() {
            return true;
        }
        @Override
        protected String getDuration0Text(){
            return "Off";
        }
    }
    
    private class NonRepeatingTimerMenu extends TimerMenu{
        
        public NonRepeatingTimerMenu(){
            super("Menu 2", 2);
        }
        @Override
        protected boolean isRepeating() {
            return false;
        }
    }
